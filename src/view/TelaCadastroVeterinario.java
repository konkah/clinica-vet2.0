    private void buttonSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonSalvarMouseClicked


        Integer id = VeterinarioDAO.getInstance().addVeterinario(
               textNome.getText(), 
               textEndereco.getText(),
               textTelefone.getText(),
               textCrvm.getText(),
              (String)comboEspecialidades.getSelectedItem());
        
        if (id > -1) {
          Veterinario veterinario = VeterinarioDAO.getInstance().getVeterinarioById(id);
          VeterinarioTableModel tb = (VeterinarioTableModel)tableDados.getModel();
          tb.addItem(veterinario);
        }
        
    }//GEN-LAST:event_buttonSalvarMouseClicked

