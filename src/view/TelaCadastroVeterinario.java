        int idVeterinario =(Integer) tableDados.getModel().getValueAt(tableDados.getSelectedRow(), 5);
       
        VeterinarioDAO.getInstance().deleteVeterinarioById(idVeterinario);
       
        tb.removeItem(tableDados.getSelectedRow());         // TODO add your handling code here:
